    subparsers.required = True

    # Sequential chain mode
    seq_parser = subparsers.add_parser('sequential',
                                       help='Generate sequential chain benchmark')
    seq_parser.add_argument('--tasks', type=int, default=15,
                           help='Number of tasks (default: 15)')
    seq_parser.add_argument('--optional-freq', type=int, default=3,
                           help='Make every Kth task optional (default: 3)')
    seq_parser.add_argument('--duration-min', type=int, default=1,
                           help='Minimum duration (default: 1)')
    seq_parser.add_argument('--duration-max', type=int, default=6,
                           help='Maximum duration (default: 6)')
    seq_parser.add_argument('--charge-rate', type=float, default=1.0,
                           help='Battery charge rate (default: 1.0)')
    seq_parser.add_argument('--requirement-step', type=float, default=5.0,
                           help='Battery requirement step (default: 5.0)')
    seq_parser.add_argument('--horizon', type=int, default=None,
                           help='Time horizon (default: tasks*10)')
    seq_parser.add_argument('--use-windows', action='store_true',
                           help='Use time windows instead of resource dependencies (generates seqforce)')

    # Parallel resource mode
    par_parser = subparsers.add_parser('parallel',
                                       help='Generate parallel resource benchmark')
    par_parser.add_argument('--tasks', type=int, default=20,
                           help='Number of tasks (default: 20)')
    par_parser.add_argument('--timelines', type=int, default=5,
                           help='Number of timelines (default: 5)')
    par_parser.add_argument('--parallelism', type=int, default=3,
                           help='Target parallelism factor (default: 3)')
    par_parser.add_argument('--horizon', type=int, default=None,
                           help='Time horizon (default: max(200, tasks*10))')

    # UNSAT mode
    unsat_parser = subparsers.add_parser('unsat',
                                         help='Generate over-constrained UNSAT benchmark')
    unsat_parser.add_argument('--tasks', type=int, required=True,
                             help='Number of tasks')
    unsat_parser.add_argument('--conflict-type', type=str, default='resource',
                             choices=['resource', 'temporal', 'dependency', 'mixed'],
                             help='Type of conflict (default: resource)')
    unsat_parser.add_argument('--horizon', type=int, default=None,
                             help='Time horizon (default: 200)')

    # Property verification mode
    prop_parser = subparsers.add_parser('properties',
                                        help='Generate property verification benchmark')
    prop_parser.add_argument('--tasks', type=int, default=10,
                            help='Number of tasks (default: 10)')
    prop_parser.add_argument('--properties', type=int, default=20,
                            help='Number of properties (default: 20)')
    prop_parser.add_argument('--horizon', type=int, default=None,
                            help='Time horizon (default: 200)')

    # Scheduling mode (time windows)
    sched_parser = subparsers.add_parser('scheduling',
                                         help='Generate temporal scheduling benchmark with time windows')
    sched_parser.add_argument('--tasks', type=int, default=15,
                             help='Number of tasks (default: 15)')
