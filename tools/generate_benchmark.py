#!/usr/bin/env python3
"""
TaskNet Benchmark Generator

Generates synthetic TaskNet benchmarks with predictable behavior for
performance evaluation and testing.
"""

import argparse
import sys
from typing import List, Tuple, Optional
from dataclasses import dataclass


@dataclass
class Timeline:
    """Timeline definition"""
    name: str
    type: str  # 'rate', 'claim', 'cumul', 'state', 'atomic'
    spec: str  # Full spec line after name and colon


@dataclass
class Task:
    """Task definition"""
    id: int
    name: str
    optional: bool
    duration: Optional[int] = None
    duration_range: Optional[Tuple[int, int]] = None
    start_range: Optional[Tuple[int, int]] = None
    end_range: Optional[Tuple[int, int]] = None
    pre: Optional[List[str]] = None
    inv: Optional[List[str]] = None
    impacts: Optional[dict] = None  # {'pre': [...], 'maint': [...], 'post': [...]}


class TaskNetGenerator:
    """Base class for generating TaskNet benchmarks"""

    def __init__(self, name: str, end: int, command: str = ""):
        self.name = name
        self.end = end
        self.command = command
        self.timelines: List[Timeline] = []
        self.tasks: List[Task] = []
        self.properties: List[Tuple[str, str]] = []  # (name, formula)
        self.header_comments: List[str] = []

    def add_timeline(self, name: str, type: str, spec: str):
        """Add a timeline definition"""
        self.timelines.append(Timeline(name, type, spec))

    def add_task(self, task: Task):
        """Add a task definition"""
        self.tasks.append(task)

    def add_property(self, name: str, formula: str):
        """Add a temporal property"""
        self.properties.append((name, formula))

    def add_header_comment(self, comment: str):
        """Add a comment line to the header"""
        self.header_comments.append(comment)

    def generate(self) -> str:
        """Generate the complete .tn file content"""
        lines = []

        # Header comments
        if self.command:
            lines.append(f"# Generated by: {self.command}")
            lines.append("#")

        for comment in self.header_comments:
            lines.append(f"# {comment}")

        if self.header_comments:
            lines.append("")

        # TaskNet declaration
        lines.append(f"tasknet {self.name} {{")
        lines.append(f"  end = {self.end};")
        lines.append("")

        # Timelines
        lines.append("  timelines {")
        for tl in self.timelines:
            lines.append(f"    {tl.name} : {tl.spec};")
        lines.append("  }")
        lines.append("")

        # Tasks
        for task in self.tasks:
            self._generate_task(lines, task)

        # Properties
        if self.properties:
            lines.append("  properties {")
            for name, formula in self.properties:
                lines.append(f"    prop {name}: {formula};")
            lines.append("  }")

        lines.append("}")
        lines.append("")

        return "\n".join(lines)

    def _generate_task(self, lines: List[str], task: Task):
        """Generate a single task definition"""
        # Task header
        optional = "optional " if task.optional else ""
        lines.append(f"  {optional}task {task.name} {{")
        lines.append(f"    id {task.id};")

        if task.duration is not None:
            lines.append(f"    duration {task.duration};")
        elif task.duration_range is not None:
            lines.append(f"    duration_range [{task.duration_range[0]}, {task.duration_range[1]}];")

        if task.start_range is not None:
            lines.append(f"    start_range [{task.start_range[0]}, {task.start_range[1]}];")

        if task.end_range is not None:
            lines.append(f"    end_range [{task.end_range[0]}, {task.end_range[1]}];")

        # Preconditions
        if task.pre:
            lines.append("    pre {")
            for cond in task.pre:
                lines.append(f"      {cond};")
            lines.append("    }")

        # Invariants
        if task.inv:
            lines.append("    inv {")
            for cond in task.inv:
                lines.append(f"      {cond};")
            lines.append("    }")

        # Impacts
        if task.impacts:
            lines.append("    impacts {")
            for phase in ['pre', 'maint', 'post']:
                if phase in task.impacts and task.impacts[phase]:
                    lines.append(f"      {phase} {{")
                    for impact in task.impacts[phase]:
                        lines.append(f"        {impact};")
                    lines.append("      }")
            lines.append("    }")

        lines.append("  }")
        lines.append("")


class SequentialChainGenerator(TaskNetGenerator):
    """Generate sequential chain benchmarks"""

    def __init__(self, num_tasks: int, optional_freq: int = 3,
                 duration_range: Tuple[int, int] = (1, 6),
                 charge_rate: float = 1.0,
                 requirement_step: float = 5.0,
                 use_time_windows: bool = False,
                 horizon: Optional[int] = None,
                 command: str = ""):

        if use_time_windows:
            horizon = horizon or num_tasks * 20
            name = f"SeqForce{num_tasks}"
        else:
            horizon = horizon or num_tasks * 10
            name = f"SequentialChain{num_tasks}"

        super().__init__(name, horizon, command)

        self.num_tasks = num_tasks
        self.optional_freq = optional_freq
        self.duration_range = duration_range
        self.charge_rate = charge_rate
        self.requirement_step = requirement_step
        self.use_time_windows = use_time_windows

        self._generate_header()
        if use_time_windows:
            self._generate_sequential_time_windows()
        else:
            self._generate_sequential_chain()

    def _generate_header(self):
        """Generate header comments"""
        num_optional = sum(1 for i in range(1, self.num_tasks + 1) if i % self.optional_freq == 0)
        num_required = self.num_tasks - num_optional

        if self.use_time_windows:
            self.add_header_comment("Sequential Force Benchmark (Resources + Time Windows)")
            self.add_header_comment("")
            self.add_header_comment(f"Configuration:")
            self.add_header_comment(f"- {self.num_tasks} tasks ({num_required} required, {num_optional} optional)")
            self.add_header_comment(f"- 1 timeline (battery)")
            self.add_header_comment(f"- Sequential ordering via BOTH resources AND time windows")
            self.add_header_comment(f"- Variable durations {list(self.duration_range)}")
            self.add_header_comment(f"- Time horizon: {self.end}")
            self.add_header_comment("")
            self.add_header_comment(f"Design principle:")
            self.add_header_comment(f"  Like seq benchmarks BUT with added temporal constraints")
            self.add_header_comment(f"  Tests scheduler with BOTH resource dependencies AND time windows")
            self.add_header_comment(f"")
            self.add_header_comment(f"Resource constraints (same as seq):")
            self.add_header_comment(f"- Battery timeline starts at 0")
            self.add_header_comment(f"- T_i requires battery >= {self.requirement_step}*(i-1)")
            self.add_header_comment(f"- Each task charges battery at +{self.charge_rate}/time")
            self.add_header_comment(f"- Forces T1 → T2 → ... → T{self.num_tasks} ordering")
            self.add_header_comment(f"")
            self.add_header_comment(f"PLUS temporal constraints (added):")
            self.add_header_comment(f"- Each task has start_range and duration_range")
            self.add_header_comment(f"- Windows arranged to prevent overlapping (sequential)")
            self.add_header_comment(f"- duration_range provides scheduling flexibility")
            self.add_header_comment(f"")
            self.add_header_comment(f"Expected behavior:")
            self.add_header_comment(f"- Tasks execute sequentially (enforced twice: resources + windows)")
            self.add_header_comment(f"- Tests combined resource + temporal reasoning")
            self.add_header_comment(f"- More constrained than seq alone")
        else:
            self.add_header_comment("Sequential Chain Benchmark (Resource Dependencies)")
            self.add_header_comment("")
            self.add_header_comment(f"Configuration:")
            self.add_header_comment(f"- {self.num_tasks} tasks ({num_required} required, {num_optional} optional)")
            self.add_header_comment(f"- 1 timeline (battery)")
            self.add_header_comment(f"- Sequential chain pattern: T1 → T2 → ... → T{self.num_tasks}")
            self.add_header_comment(f"- Variable durations {list(self.duration_range)}")
            self.add_header_comment("")
            self.add_header_comment(f"Expected behavior:")
            self.add_header_comment(f"- Tasks must execute sequentially")
            self.add_header_comment(f"- T_i requires battery >= {self.requirement_step}*(i-1)")
            self.add_header_comment(f"- Each task charges battery at +{self.charge_rate}/time")

    def _generate_sequential_chain(self):
        """Generate sequential chain pattern"""
        # Add battery timeline
        self.add_timeline("battery", "rate",
                         "rate [0.0, 200.0] bounds [0.0, 200.0] = 0.0")

        # Generate tasks T1, T2, ..., TN
        for i in range(1, self.num_tasks + 1):
            battery_req = (i - 1) * self.requirement_step
            optional = (i % self.optional_freq == 0)

            task = Task(
                id=i,
                name=f"T{i}",
                optional=optional,
                duration=None,
                duration_range=self.duration_range,
                pre=[f"battery in [{battery_req}, 200.0]"],
                inv=[f"battery in [{battery_req}, 200.0]"],
                impacts={
                    'maint': [f"battery +~ {self.charge_rate}"]
                }
            )
            self.add_task(task)

        # Add temporal constraint: final task must eventually execute
        self.add_property(f"force_t{self.num_tasks}",
                         f"eventually active(T{self.num_tasks})")

    def _generate_sequential_time_windows(self):
        """Generate sequential chain WITH time windows (both resource + temporal constraints)"""
        # Add battery timeline (same as regular seq)
        self.add_timeline("battery", "rate",
                         "rate [0.0, 200.0] bounds [0.0, 200.0] = 0.0")

        # Calculate time slot for each task
        max_duration = self.duration_range[1]
        time_slot = max_duration + 2  # Ensure no overlap: max duration + buffer

        for i in range(1, self.num_tasks + 1):
            battery_req = (i - 1) * self.requirement_step
            optional = (i % self.optional_freq == 0)

            # Task i occupies time slot [(i-1)*slot, i*slot]
            slot_start = (i - 1) * time_slot

            # start_range: narrow window at start of slot (±1 for flexibility)
            start_min = slot_start
            start_max = slot_start + 2

            # No end_range needed - it's determined by start_range + duration_range

            task = Task(
                id=i,
                name=f"T{i}",
                optional=optional,
                duration=None,
                duration_range=self.duration_range,
                start_range=[start_min, start_max],
                pre=[f"battery in [{battery_req}, 200.0]"],
                inv=[f"battery in [{battery_req}, 200.0]"],
                impacts={
                    'maint': [f"battery +~ {self.charge_rate}"]
                }
            )
            self.add_task(task)

        # Add temporal constraint: final task must eventually execute
        self.add_property(f"force_t{self.num_tasks}",
                         f"eventually active(T{self.num_tasks})")


class ParallelResourceGenerator(TaskNetGenerator):
    """Generate parallel resource benchmarks"""

    def __init__(self, num_tasks: int, num_timelines: int = 5,
                 parallelism: int = 3,
                 horizon: Optional[int] = None,
                 command: str = ""):

        horizon = horizon or max(200, num_tasks * 10)
        super().__init__(f"ParallelResource{num_tasks}_{num_timelines}",
                        horizon, command)

        self.num_tasks = num_tasks
        self.num_timelines = num_timelines
        self.parallelism = parallelism

        self._generate_header()
        self._generate_parallel_resources()

    def _generate_header(self):
        """Generate header comments"""
        self.add_header_comment("Parallel Resource Benchmark (Concurrency Counters)")
        self.add_header_comment("")
        self.add_header_comment(f"Configuration:")
        self.add_header_comment(f"- {self.num_tasks} tasks")
        self.add_header_comment(f"- {self.num_timelines} timelines (concurrency counters)")
        self.add_header_comment(f"- Max parallelism per timeline: {self.parallelism} concurrent tasks")
        self.add_header_comment(f"- Time horizon: {self.end}")
        self.add_header_comment("")
        self.add_header_comment(f"Design pattern - Concurrency counter:")
        self.add_header_comment(f"- Each timeline is a counter (cumulative timeline, starts at 0)")
        self.add_header_comment(f"- Tasks on that timeline:")
        self.add_header_comment(f"  - pre: counter <= {self.parallelism-1} (can start if < {self.parallelism} tasks running)")
        self.add_header_comment(f"  - inv: counter <= {self.parallelism} (allows incrementing during task)")
        self.add_header_comment(f"  - impacts.pre: counter += 1 (increment on task start)")
        self.add_header_comment(f"  - impacts.post: counter -= 1 (decrement on task end)")
        self.add_header_comment(f"")
        self.add_header_comment(f"Task distribution:")
        self.add_header_comment(f"- Tasks distributed round-robin across {self.num_timelines} timelines")
        self.add_header_comment(f"- T1→tl1, T2→tl2, ..., T{self.num_timelines}→tl{self.num_timelines}")
        self.add_header_comment(f"- T{self.num_timelines+1}→tl1, T{self.num_timelines+2}→tl2, ...")
        tasks_per_tl = (self.num_tasks + self.num_timelines - 1) // self.num_timelines  # ceil division
        self.add_header_comment(f"- ~{tasks_per_tl} tasks per timeline average")
        self.add_header_comment("")
        self.add_header_comment(f"Guaranteed parallelism:")
        self.add_header_comment(f"- Each timeline allows up to {self.parallelism} concurrent tasks")
        self.add_header_comment(f"- With {self.num_timelines} timelines: up to {self.num_timelines * self.parallelism} tasks can run simultaneously")
        self.add_header_comment(f"- Tasks on different timelines are completely independent")
        self.add_header_comment("")
        self.add_header_comment(f"Example execution:")
        self.add_header_comment(f"- Initially: tl1=0, tl2=0, ...")
        self.add_header_comment(f"- T1 starts: tl1=0 ✓ (≤{self.parallelism-1}), tl1 becomes 1")
        self.add_header_comment(f"- T2 starts: tl2=0 ✓, tl2 becomes 1")
        self.add_header_comment(f"- T{self.num_timelines+1} starts: tl1=1 ✓, tl1 becomes 2")
        if self.parallelism >= 3:
            self.add_header_comment(f"- T{2*self.num_timelines+1} starts: tl1=2 ✓, tl1 becomes 3 (max reached)")
            self.add_header_comment(f"- Next task on tl1 must wait until one finishes")
        self.add_header_comment("")
        self.add_header_comment(f"Expected behavior:")
        self.add_header_comment(f"- SAT (valid schedule exists)")
        self.add_header_comment(f"- Parallelism is predictable and verifiable")
        self.add_header_comment(f"- Should solve much faster than equivalent sequential chain")

    def _generate_parallel_resources(self):
        """Generate parallel resource pattern using concurrency counters"""
        # Create timelines as concurrency counters (cumulative timelines)
        max_concurrent = self.parallelism

        for i in range(1, self.num_timelines + 1):
            # Cumulative timeline: starts at 0, range [0, max_concurrent]
            spec = f"cumulative [0.0, {float(max_concurrent)}] = 0.0"
            self.add_timeline(f"tl{i}", "cumulative", spec)

        # Generate tasks distributed across timelines
        for i in range(1, self.num_tasks + 1):
            # Assign task to timeline - use modulo to distribute round-robin
            primary_tl = ((i - 1) % self.num_timelines) + 1
            tl_name = f"tl{primary_tl}"

            # Concurrency counter pattern:
            # - pre: counter <= K-1 (can start if less than K tasks running)
            # - inv: counter <= K (allows the increment during task)
            # - impacts.pre: counter += 1 (increment on start)
            # - impacts.post: counter -= 1 (decrement on end)

            pre = [f"{tl_name} in [0.0, {float(max_concurrent - 1)}]"]
            inv = [f"{tl_name} in [0.0, {float(max_concurrent)}]"]
            impacts = {
                'pre': [f"{tl_name} += 1.0"],
                'post': [f"{tl_name} += -1.0"]
            }

            # Use duration_range (hard constraint) not duration (soft constraint)
            # Varying durations 10-30 based on task number
            task_duration = 10 + (i % 10) * 2

            task = Task(
                id=i,
                name=f"T{i}",
                optional=False,
                duration=None,
                duration_range=[task_duration, task_duration],
                pre=pre,
                inv=inv,
                impacts=impacts
            )
            self.add_task(task)


